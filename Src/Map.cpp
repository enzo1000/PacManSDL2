Map::Map() {
	path = TextureManager::LoadTexture("assets/path.png");
	wall = TextureManager::LoadTexture("assets/wall.png");
    level = TextureManager::LoadTexture("assets/level.png");

    LoadMap(level1);

    src.x = src.y = 0;
    src.w = dest.w = 31;
    src.h = dest.h = 31;

    dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[mapHeight][mapWidth]) {
    for (int row = 0; row < mapHeight; row++) {
        for (int column = 0; column < mapWidth; column++) {
            map[row][column] = arr[row][column];
        }
    }
}

void Map::DrawMap() {
    int type = 0;
    for (int row = 0; row < mapHeight; row++) {
        for (int column = 0; column < mapWidth; column++) {
            type = map[row][column];

            dest.x = column * 31;
            dest.y = row * 31;

            switch (type) {

            case 1:
                TextureManager::Draw(wall, src, dest);
                break;

            case 2:
                TextureManager::Draw(path, src, dest);
                break;

            default:
                break;
            }
        }
    }
    //We drawing our pacman map in front of our tile map
    TextureManager::Draw(level, SDL_Rect{0, 0, 651, 713}, SDL_Rect{0, 0, 651, 713 });
}